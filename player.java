
public class player {
	String name;
	int Position;
	int Tackles;
	int PassingYards;
	int RushingYards;
	int Sacks;
	int ReceivingYards;
	int Interceptions;
	int Touchdowns;
	
	public void setName(String n) {
		name = n;
	}
	public String getName() {
		return name;
	}
	public void setPosition(int p) {
		Position = p;
	}
	public int getPosition() {
		return Position;
	}

}
